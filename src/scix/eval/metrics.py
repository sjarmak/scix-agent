"""Three-way eval metrics facade for PRD §M4.

This module is a thin wrapper over :mod:`scix.ir_metrics` that adds the
three-way-configuration runner used by ``scripts/eval_three_way.py`` to
compare (a) hybrid baseline, (b) hybrid + static-core entity filter, and
(c) hybrid + JIT enrichment on the M4 fixture.

Design notes
------------
* The retrieval component for each configuration is supplied as a callable
  so the same runner can be used by the fixture-based pytest (deterministic
  seeded results) and the real scix_test runner.
* Metrics (nDCG@10, Recall@20, MRR) come from :mod:`scix.ir_metrics` — no
  reimplementation here.
* The in-house disclaimer is exposed as a module constant so tests can
  assert its presence in the generated report.
"""

from __future__ import annotations

import time
from dataclasses import dataclass, field
from typing import Callable

from scix.ir_metrics import (
    EvalReport,
    RetrievalScore,
    aggregate_scores,
    compute_retrieval_score,
    mean_reciprocal_rank,
    ndcg_at_k,
    recall_at_k,
)

# ---------------------------------------------------------------------------
# Public constants
# ---------------------------------------------------------------------------

INHOUSE_DISCLAIMER = (
    "> **In-house authored evaluation.** This report was generated by the "
    "SciX team without external human validators. All queries, relevance "
    "judgments, and retrieval systems were authored by the same engineering "
    "team that built the retrieval stack. Results are self-reported and "
    "should be interpreted accordingly — they are a useful engineering "
    "signal, not an independent third-party benchmark."
)


@dataclass(frozen=True)
class QueryFixture:
    """A single fixture query used by the three-way eval.

    Attributes
    ----------
    query_id
        Stable identifier for reporting and test assertions.
    query_text
        Human-readable query string (ignored by the fixture runner; kept
        for report generation).
    relevant_bibcodes
        Set of bibcodes considered relevant for this query. Used for
        Recall@K / MRR.
    relevance_map
        Optional graded-relevance mapping. If ``None`` the runner falls
        back to binary relevance derived from ``relevant_bibcodes``.
    """

    query_id: str
    query_text: str
    relevant_bibcodes: frozenset[str]
    relevance_map: dict[str, float] | None = None


@dataclass(frozen=True)
class GraphWalkTask:
    """A single graph-walk task for §M4 secondary metric set."""

    task_id: str
    seed_bibcode: str
    expected_bibcodes: frozenset[str]


# ``Retriever`` is the contract every configuration must satisfy.
# It takes a :class:`QueryFixture` and returns (ordered_bibcodes, latency_ms).
Retriever = Callable[[QueryFixture], tuple[list[str], float]]


@dataclass(frozen=True)
class ThreeWayConfig:
    """One of the three configurations evaluated in §M4.

    Attributes
    ----------
    name
        Configuration identifier — one of ``hybrid_baseline``,
        ``hybrid_plus_static``, ``hybrid_plus_jit``.
    description
        Human-readable summary for the report.
    retrieve
        Callable that runs retrieval for a single query.
    """

    name: str
    description: str
    retrieve: Retriever


@dataclass(frozen=True)
class ThreeWayResults:
    """Aggregated outputs from :func:`run_three_way_eval`."""

    query_reports: dict[str, EvalReport]
    graph_walk_reports: dict[str, EvalReport]
    per_query_scores: dict[str, list[RetrievalScore]] = field(default_factory=dict)
    per_task_scores: dict[str, list[RetrievalScore]] = field(default_factory=dict)


# ---------------------------------------------------------------------------
# Runner
# ---------------------------------------------------------------------------


def _relevance_map(fixture: QueryFixture) -> dict[str, float]:
    if fixture.relevance_map is not None:
        return dict(fixture.relevance_map)
    return {bib: 1.0 for bib in fixture.relevant_bibcodes}


def _graph_walk_to_fixture(task: GraphWalkTask) -> QueryFixture:
    return QueryFixture(
        query_id=task.task_id,
        query_text=f"graph_walk:{task.seed_bibcode}",
        relevant_bibcodes=task.expected_bibcodes,
    )


def run_three_way_eval(
    queries: list[QueryFixture],
    graph_walk_tasks: list[GraphWalkTask],
    configs: list[ThreeWayConfig],
) -> ThreeWayResults:
    """Run each configuration against the queries and graph-walk tasks.

    Parameters
    ----------
    queries
        50 (or 5 on the fixture) seeded queries.
    graph_walk_tasks
        20 (or 2 on the fixture) graph-walk tasks.
    configs
        Exactly three :class:`ThreeWayConfig` objects.

    Returns
    -------
    ThreeWayResults
        Per-config :class:`EvalReport` objects, separately for the query
        set and the graph-walk set, plus per-query score lists.
    """
    if len(configs) != 3:
        raise ValueError(f"M4 eval requires exactly 3 configs, got {len(configs)}")

    per_query_scores: dict[str, list[RetrievalScore]] = {c.name: [] for c in configs}
    per_task_scores: dict[str, list[RetrievalScore]] = {c.name: [] for c in configs}

    for fixture in queries:
        rel_map = _relevance_map(fixture)
        for config in configs:
            t0 = time.perf_counter()
            retrieved, latency_ms = config.retrieve(fixture)
            if latency_ms <= 0:
                latency_ms = (time.perf_counter() - t0) * 1000.0
            score = compute_retrieval_score(
                query_id=fixture.query_id,
                system=config.name,
                retrieved_ids=retrieved,
                relevance_map=rel_map,
                latency_ms=latency_ms,
            )
            per_query_scores[config.name].append(score)

    for task in graph_walk_tasks:
        fixture = _graph_walk_to_fixture(task)
        rel_map = _relevance_map(fixture)
        for config in configs:
            t0 = time.perf_counter()
            retrieved, latency_ms = config.retrieve(fixture)
            if latency_ms <= 0:
                latency_ms = (time.perf_counter() - t0) * 1000.0
            score = compute_retrieval_score(
                query_id=task.task_id,
                system=config.name,
                retrieved_ids=retrieved,
                relevance_map=rel_map,
                latency_ms=latency_ms,
            )
            per_task_scores[config.name].append(score)

    query_reports = {
        name: aggregate_scores(name, scores) for name, scores in per_query_scores.items()
    }
    graph_walk_reports = {
        name: aggregate_scores(name, scores) for name, scores in per_task_scores.items()
    }

    return ThreeWayResults(
        query_reports=query_reports,
        graph_walk_reports=graph_walk_reports,
        per_query_scores=per_query_scores,
        per_task_scores=per_task_scores,
    )


# ---------------------------------------------------------------------------
# Report formatting
# ---------------------------------------------------------------------------


def format_m4_report(
    results: ThreeWayResults,
    configs: list[ThreeWayConfig],
    n_queries: int,
    n_graph_walk: int,
    timestamp: str | None = None,
) -> str:
    """Render the §M4 markdown report.

    The first two lines are always the in-house disclaimer (AC2).
    """
    when = timestamp or time.strftime("%Y-%m-%d %H:%M:%S")
    lines: list[str] = []
    lines.append(INHOUSE_DISCLAIMER)
    lines.append("")
    lines.append("# M4 — Three-Way Entity-Enrichment Eval")
    lines.append("")
    lines.append(f"**Generated**: {when}")
    lines.append(f"**Queries**: {n_queries} seeded queries")
    lines.append(f"**Graph-walk tasks**: {n_graph_walk}")
    lines.append("")

    lines.append("## Configurations")
    lines.append("")
    for config in configs:
        lines.append(f"- **{config.name}** — {config.description}")
    lines.append("")

    lines.append("## Query-set results")
    lines.append("")
    lines.append("| Config | nDCG@10 | Recall@20 | MRR | Latency (ms) |")
    lines.append("|--------|---------|-----------|-----|--------------|")
    for config in configs:
        report = results.query_reports[config.name]
        lines.append(
            f"| {config.name} | {report.mean_ndcg_at_10:.4f} "
            f"| {report.mean_recall_at_20:.4f} | {report.mean_mrr:.4f} "
            f"| {report.mean_latency_ms:.2f} |"
        )
    lines.append("")

    lines.append("## Graph-walk task results")
    lines.append("")
    lines.append("| Config | nDCG@10 | Recall@20 | MRR | Latency (ms) |")
    lines.append("|--------|---------|-----------|-----|--------------|")
    for config in configs:
        report = results.graph_walk_reports[config.name]
        lines.append(
            f"| {config.name} | {report.mean_ndcg_at_10:.4f} "
            f"| {report.mean_recall_at_20:.4f} | {report.mean_mrr:.4f} "
            f"| {report.mean_latency_ms:.2f} |"
        )
    lines.append("")

    lines.append("## Methodology")
    lines.append("")
    lines.append("- nDCG@10 / Recall@20 / MRR computed via `scix.ir_metrics`.")
    lines.append("- Retrieval pools for the fixture eval are deterministic, seeded lists.")
    lines.append(
        "- Entity-enrichment configurations route through `scix.resolve_entities` per §M13."
    )
    lines.append("")

    lines.append("## Lane deltas (vs hybrid_baseline)")
    lines.append("")
    lines.append("| Config | ΔnDCG@10 | ΔRecall@20 | ΔMRR |")
    lines.append("|--------|----------|------------|------|")
    baseline_q = results.query_reports.get("hybrid_baseline")
    if baseline_q is not None:
        for config in configs:
            report = results.query_reports[config.name]
            d_ndcg = report.mean_ndcg_at_10 - baseline_q.mean_ndcg_at_10
            d_recall = report.mean_recall_at_20 - baseline_q.mean_recall_at_20
            d_mrr = report.mean_mrr - baseline_q.mean_mrr
            lines.append(
                f"| {config.name} | {d_ndcg:+.4f} | {d_recall:+.4f} | {d_mrr:+.4f} |"
            )
    lines.append("")

    return "\n".join(lines) + "\n"


