"""Integration tests for the eager mini-summary on ``entity(action='resolve')``.

Covers bead ``scix_experiments-3lw4.1``: ``co_occurring`` and
``recent_examples`` fields folded into resolver candidates so callers can
triage without an immediate ``entity_context`` follow-up.

Tests seed a small graph into ``entities`` + ``papers`` +
``document_entities`` (the source table for the canonical matview), refresh
``document_entities_canonical``, and then exercise the helper directly and
through ``_handle_entity``. The matview rebuild is fast on ``scix_test``
(<100 rows) but skipped cleanly when ``SCIX_TEST_DSN`` is not configured
(production DSN is rejected).
"""

from __future__ import annotations

import json
from collections.abc import Iterator
from typing import Any

import psycopg
import pytest

from scix.entity_eager_summary import fetch_entity_mini_summaries
from scix.mcp_server import _handle_entity
from tests.helpers import get_test_dsn

# Stable string prefix used across all seeded rows so teardown can remove
# only the rows this fixture created. Test entity ids are generated by the
# entities sequence; we capture them in the fixture and pass them down.
_BIB_PREFIX = "TEST-EAGER-SUM-"
_ENTITY_NAME_PREFIX = "TestEagerSum-"
_ENTITY_SOURCE = "test-eager-sum"


def _cleanup(conn: psycopg.Connection) -> None:
    """Remove any rows this fixture may have created on prior runs."""
    conn.rollback()
    with conn.cursor() as cur:
        cur.execute(
            "DELETE FROM document_entities WHERE bibcode LIKE %s",
            (f"{_BIB_PREFIX}%",),
        )
        cur.execute(
            "DELETE FROM papers WHERE bibcode LIKE %s",
            (f"{_BIB_PREFIX}%",),
        )
        cur.execute(
            "DELETE FROM entities WHERE source = %s",
            (_ENTITY_SOURCE,),
        )
    conn.commit()
    # Refresh the matview so leftover seeded rows from a crashed prior run
    # do not show up in helper queries before we re-seed.
    with conn.cursor() as cur:
        cur.execute("REFRESH MATERIALIZED VIEW document_entities_canonical")
    conn.commit()


def _seed(conn: psycopg.Connection) -> dict[str, int]:
    """Seed a small entity-co-occurrence graph for the test cases.

    Layout:
      Entity FOCAL -- appears in 5 papers (P1..P5)
      Entity COA   -- appears in P1, P2, P3 (3 shared with FOCAL)
      Entity COB   -- appears in P1, P2          (2 shared with FOCAL)
      Entity COC   -- appears in P1               (1 shared, below threshold)
      Entity ISOLATED -- appears only in its own paper (P_ISO),
                         no overlap with FOCAL.

    Returns a dict mapping logical name -> entity_id.
    """
    with conn.cursor() as cur:
        # Insert entities first so we get back the auto-generated ids.
        # entity_type values are picked from the production enum list so
        # any downstream constraints behave the same way they do in prod.
        names = ["FOCAL", "COA", "COB", "COC", "ISOLATED"]
        types = ["method", "method", "dataset", "instrument", "method"]
        ids: dict[str, int] = {}
        for n, t in zip(names, types):
            cur.execute(
                """
                INSERT INTO entities
                  (canonical_name, entity_type, source)
                VALUES (%s, %s, %s)
                RETURNING id
                """,
                (f"{_ENTITY_NAME_PREFIX}{n}", t, _ENTITY_SOURCE),
            )
            ids[n] = cur.fetchone()[0]

        # Papers: P1..P5 spread across years so recent_examples ordering
        # has meaningful signal (year DESC).
        # P_ISO is for the ISOLATED entity only.
        papers = [
            (f"{_BIB_PREFIX}P1", "Paper 1", 2025),
            (f"{_BIB_PREFIX}P2", "Paper 2", 2024),
            (f"{_BIB_PREFIX}P3", "Paper 3", 2023),
            (f"{_BIB_PREFIX}P4", "Paper 4", 2022),
            (f"{_BIB_PREFIX}P5", "Paper 5", 2021),
            (f"{_BIB_PREFIX}P_ISO", "Isolated Paper", 2020),
        ]
        for bib, title, year in papers:
            cur.execute(
                "INSERT INTO papers (bibcode, title, year) VALUES (%s, %s, %s)",
                (bib, title, year),
            )

        # document_entities links. Use confidence=0.9 and tier=0 so the
        # matview's fusion math produces a non-zero fused_confidence.
        # link_type='instance' is a generic value that satisfies the NOT
        # NULL constraint without depending on a specific harvester.
        de_rows = [
            # FOCAL appears in P1..P5
            ("FOCAL", "P1"),
            ("FOCAL", "P2"),
            ("FOCAL", "P3"),
            ("FOCAL", "P4"),
            ("FOCAL", "P5"),
            # COA shares 3 papers with FOCAL
            ("COA", "P1"),
            ("COA", "P2"),
            ("COA", "P3"),
            # COB shares 2 papers with FOCAL (at threshold)
            ("COB", "P1"),
            ("COB", "P2"),
            # COC shares 1 paper with FOCAL (below default threshold)
            ("COC", "P1"),
            # ISOLATED has its own paper, no overlap with FOCAL.
            ("ISOLATED", "P_ISO"),
        ]
        for ename, pname in de_rows:
            cur.execute(
                """
                INSERT INTO document_entities
                  (bibcode, entity_id, link_type, confidence, tier, tier_version)
                VALUES (%s, %s, 'instance', 0.9, 0, 1)
                """,
                (f"{_BIB_PREFIX}{pname}", ids[ename]),
            )

    conn.commit()

    # Refresh the matview so the seeded rows propagate to
    # document_entities_canonical (the helper reads this matview, not
    # document_entities directly).
    with conn.cursor() as cur:
        cur.execute("REFRESH MATERIALIZED VIEW document_entities_canonical")
    conn.commit()

    return ids


@pytest.fixture()
def seeded_conn() -> Iterator[tuple[psycopg.Connection, dict[str, int]]]:
    """Yield a connection with the test graph seeded; clean up after."""
    dsn = get_test_dsn()
    if dsn is None:
        pytest.skip("SCIX_TEST_DSN not set or points at production")
    conn = psycopg.connect(dsn)
    conn.autocommit = False
    try:
        _cleanup(conn)
        ids = _seed(conn)
        yield conn, ids
    finally:
        try:
            _cleanup(conn)
        finally:
            conn.close()


