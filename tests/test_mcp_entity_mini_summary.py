def _resolve(conn: psycopg.Connection, query: str) -> dict[str, Any]:
    return json.loads(
        _handle_entity(
            conn,
            {"action": "resolve", "query": query, "fuzzy": False},
        )
    )


def test_resolve_response_includes_mini_summary_fields(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """Each candidate row exposes co_occurring and recent_examples keys."""
    conn, _ = seeded_conn
    parsed = _resolve(conn, f"{_ENTITY_NAME_PREFIX}FOCAL")
    cands = parsed.get("candidates", [])
    # Resolver must return at least the seeded FOCAL row.
    assert any(c["canonical_name"] == f"{_ENTITY_NAME_PREFIX}FOCAL" for c in cands)
    for c in cands:
        assert "co_occurring" in c
        assert "recent_examples" in c
    focal = next(c for c in cands if c["canonical_name"] == f"{_ENTITY_NAME_PREFIX}FOCAL")
    assert focal["co_occurring"] is not None
    assert focal["recent_examples"] is not None
    # Sanity: top co-occurring entity is COA per the seed.
    assert focal["co_occurring"][0]["canonical_name"] == f"{_ENTITY_NAME_PREFIX}COA"
    # Recent examples ordered year DESC.
    assert focal["recent_examples"][0]["bibcode"] == f"{_BIB_PREFIX}P1"


def test_resolve_response_emits_null_for_isolated_entity(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """An entity with no co-occurrences surfaces co_occurring=null."""
    conn, _ = seeded_conn
    parsed = _resolve(conn, f"{_ENTITY_NAME_PREFIX}ISOLATED")
    iso = next(
        c
        for c in parsed.get("candidates", [])
        if c["canonical_name"] == f"{_ENTITY_NAME_PREFIX}ISOLATED"
    )
    assert iso["co_occurring"] is None
    # ISOLATED still has its own paper as a recent example.
    assert iso["recent_examples"] is not None
    assert iso["recent_examples"][0]["bibcode"] == f"{_BIB_PREFIX}P_ISO"
