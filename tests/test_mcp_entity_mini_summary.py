def test_helper_empty_input_returns_empty_dict() -> None:
    """No DB call needed when entity_ids is empty; helper returns {}."""
    # Pass a None-ish connection to prove the helper short-circuits before
    # touching it.
    result = fetch_entity_mini_summaries(None, [])  # type: ignore[arg-type]
    assert result == {}


def test_helper_co_occurring_top_k_and_threshold(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """FOCAL gets COA (3 shared) and COB (2 shared); COC (1 shared) drops."""
    conn, ids = seeded_conn
    summaries = fetch_entity_mini_summaries(
        conn,
        [ids["FOCAL"]],
        co_occur_min=2,
        co_occur_top_k=3,
        recent_top_k=2,
    )
    co = summaries[ids["FOCAL"]]["co_occurring"]
    assert co is not None
    names = [c["canonical_name"] for c in co]
    assert f"{_ENTITY_NAME_PREFIX}COA" in names
    assert f"{_ENTITY_NAME_PREFIX}COB" in names
    assert f"{_ENTITY_NAME_PREFIX}COC" not in names, "below-threshold should drop"
    # COA must outrank COB on shared_papers count.
    coa_idx = names.index(f"{_ENTITY_NAME_PREFIX}COA")
    cob_idx = names.index(f"{_ENTITY_NAME_PREFIX}COB")
    assert coa_idx < cob_idx
    # shared_papers is the count of co-occurrences in the sampled paper set.
    coa_row = co[coa_idx]
    cob_row = co[cob_idx]
    assert coa_row["shared_papers"] == 3
    assert cob_row["shared_papers"] == 2


def test_helper_recent_examples_orders_by_year_desc(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """Recent examples come back year-DESC, capped at recent_top_k=2."""
    conn, ids = seeded_conn
    summaries = fetch_entity_mini_summaries(
        conn,
        [ids["FOCAL"]],
        recent_top_k=2,
    )
    recent = summaries[ids["FOCAL"]]["recent_examples"]
    assert recent is not None
    assert len(recent) == 2
    years = [r["year"] for r in recent]
    assert years == sorted(years, reverse=True)
    # The most-recent FOCAL paper is P1 (year=2025).
    assert recent[0]["bibcode"] == f"{_BIB_PREFIX}P1"
    assert recent[0]["year"] == 2025
    assert recent[0]["title"] == "Paper 1"


def test_helper_absent_data_emits_null_not_empty_list(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """ISOLATED has no co-occurrences -> co_occurring is None, not []."""
    conn, ids = seeded_conn
    summaries = fetch_entity_mini_summaries(
        conn,
        [ids["ISOLATED"]],
        co_occur_min=2,
    )
    assert summaries[ids["ISOLATED"]]["co_occurring"] is None
    # ISOLATED still has its own paper, so recent_examples is non-empty.
    recent = summaries[ids["ISOLATED"]]["recent_examples"]
    assert recent is not None
    assert recent[0]["bibcode"] == f"{_BIB_PREFIX}P_ISO"


def test_helper_unknown_entity_id_keys_present_with_nulls(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """Unknown entity_ids must still appear in the result dict, both fields null."""
    conn, _ = seeded_conn
    bogus = -999_999
    summaries = fetch_entity_mini_summaries(conn, [bogus])
    assert summaries[bogus] == {"co_occurring": None, "recent_examples": None}


def test_helper_high_threshold_drops_all_co_occurring(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """A precision-style filter that empties the field surfaces None."""
    conn, ids = seeded_conn
    # No co-occurring entity shares >=10 papers with FOCAL in this seed.
    summaries = fetch_entity_mini_summaries(
        conn,
        [ids["FOCAL"]],
        co_occur_min=10,
    )
    assert summaries[ids["FOCAL"]]["co_occurring"] is None
    # recent_examples is unaffected by the co-occur threshold.
    assert summaries[ids["FOCAL"]]["recent_examples"] is not None


def test_helper_batched_query_handles_multiple_focals(
    seeded_conn: tuple[psycopg.Connection, dict[str, int]],
) -> None:
    """One round trip enriches both FOCAL and ISOLATED with their own data."""
    conn, ids = seeded_conn
    summaries = fetch_entity_mini_summaries(conn, [ids["FOCAL"], ids["ISOLATED"]])
    assert ids["FOCAL"] in summaries
    assert ids["ISOLATED"] in summaries
    assert summaries[ids["FOCAL"]]["co_occurring"] is not None
    assert summaries[ids["ISOLATED"]]["co_occurring"] is None


